        public static void Window()
        {
            Console.SetWindowSize(100, 30);
            Console.SetBufferSize(100, 30);

            IntPtr hwnd = Imports.FindWindow("ConsoleWindowClass", null);
            if (hwnd != null)
            {
                Imports.DeleteMenu(Imports.GetSystemMenu(Imports.GetConsoleWindow(), false), Variables.scMaximize, Variables.mfBycommand);
                Imports.DeleteMenu(Imports.GetSystemMenu(Imports.GetConsoleWindow(), false), Variables.scSize, Variables.mfBycommand);
                Imports.SetWindowLong(
                    hwnd,
                    Variables.gwlStyle,
                    Imports.GetWindowLong(hwnd, Variables.gwlStyle) ^ Variables.whThickframe
                );
            }
        }
        public static void Title()
        {
